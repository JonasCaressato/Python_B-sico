"""
Enunciado exercício 03 - Somnando dois números:

Crie um programa que leia dois números e mostre a soma entre eles.
"""

numero_01 = int(input('Digite um valor: '))
numero_02 = int(input('Digite um outro valor: '))

print(f'A soma entre os valores: {numero_01} e {numero_02} é igual a {numero_01 + numero_02} !!!')
